function handles = gui_gbinc_buttons_vickers_indents(x0, hu, wu)
%% Function to create buttons/boxes for the loading/setting of images
% x0: origin of x coordinate
% hu: heigth unit
% wu: width unit

% authors: d.mercier@mpie.de / c.zambaldi@mpie.de

handles = uibuttongroup('visible','on','Position',[x0 hu*3.2 wu*3 x0*8]);

parent = handles;
x0 = 0.01;
hu = 0.1; % height unit
wu = 0.1; % width unit

%% Get Vickers distance
handles.distance_calculation = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'pushbutton',...
    'Position', [x0 hu wu*3 x0*80],...
    'String', 'VICKERS DISTANCE',...
    'Callback', 'gui_gbinc_vickers_distance');

handles.indent_str = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'text',...
    'Position', [x0*32 hu*5 wu*2 x0*40],...
    'String', 'Vickers indent',...
    'HorizontalAlignment', 'left');

handles.indent_selection = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'popupmenu',...
    'Position', [x0*32 hu*2 wu*2 x0*40],...
    'String', 'edge|ridge',...
    'BackgroundColor', [1 1 1],...
    'Value', 1);

%% Get thickness of material removed
handles.height_calculation = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'pushbutton',...
    'Position', [x0*55 hu wu*2 x0*80],...
    'String', 'CALCULATION',...
    'Callback', 'gui_gbinc_thickness_removed_calculation');

handles.height_str = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'text',...
    'Position', [x0*75 hu*5 wu*1.5 x0*40],...
    'String', 'Thickness',...
    'HorizontalAlignment', 'left');

handles.height = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'edit',...
    'Position', [x0*75 hu*2 wu*1.5 x0*40],...
    'BackgroundColor', [1 1 1]);

handles.height_unit = uicontrol('Parent', parent,...
    'Units', 'normalized',...
    'Style', 'text',...
    'Position', [x0*90 hu*2 wu x0*40],...
    'String', 'µm',...
    'HorizontalAlignment', 'left');

end